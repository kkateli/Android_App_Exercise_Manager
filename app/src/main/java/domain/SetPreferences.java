public class SetPreferences {
    private String exercisePreference;
    private String learningPreference;
    private String relaxationPreference;

    public String getExercisePreference() {
        return exercisePreference;
    }

    public void setExercisePreference(String exercisePreference) {
        this.exercisePreference = exercisePreference;
    }

    public String getLearningPreference() {
        return learningPreference;
    }

    public void setLearningPreference(String learningPreference) {
        this.learningPreference = learningPreference;
    }

    public String getRelaxationPreference() {
        return relaxationPreference;
    }

    public void setRelaxationPreference(String relaxationPreference) {
        this.relaxationPreference = relaxationPreference;
    }
}
